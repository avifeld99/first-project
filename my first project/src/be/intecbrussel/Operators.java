package be.intecbrussel;

public class Operators {

    public static void main(String[]args) {

        int myNumber = 10;
        myNumber++;
        ++myNumber;
        System.out.println(myNumber++); //Increment
        System.out.println(myNumber);

        System.out.println(--myNumber); //decrement
        System.out.println(myNumber--); //wordt niet afgeprint maar wel in de app


    }
}
